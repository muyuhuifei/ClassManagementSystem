package com.fonxian.Model;

public class Teacher {
	private int tid;
	private String tname;
	private int ttel;
	public int getTid() {
		return tid;
	}
	public void setTid(int tid) {
		this.tid = tid;
	}
	public String getTname() {
		return tname;
	}
	public void setTname(String tname) {
		this.tname = tname;
	}
	public int getTtel() {
		return ttel;
	}
	public void setTtel(int ttel) {
		this.ttel = ttel;
	}
	
}
